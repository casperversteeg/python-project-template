def add(a: int, b: int) -> int:
    """Add two numbers.

    This is an example Docstring. Recommended format is the Google style.

    Parameters
    ----------
    a : int
        The first number to add.
    b : int
        The second number to add.

    Returns
    -------
    int
        The sum of the two arguments, :math:`a + b`.
    """
    return a + b
